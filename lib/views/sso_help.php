<h3>Introduction to SAML</h3>
<p>There are a number of great SAML documents out there (Wikipedia included), but here's a quick run-through if this is your first time working with the protocol:</p>
<p>There are two basic entities involved in a SAML relationship: the <strong>Identity Provider (IdP)</strong> and the <strong>Service Provider (SP)</strong>.  This WordPress website is the service provider, and your company's SSO portal (or vendor's, if you outsource it) is the Identity Provider.</p>